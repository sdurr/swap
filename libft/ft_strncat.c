#include <stdlib.h>
#include <string.h>

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int i;

	i = 0;
	if (s1 || s2)
	{
		while (s1[i] != '\0')
			i++;
		while ((*s2 != '\0') && (n != 0))
		{
			s1[i] = *s2;
			i++;
			s2++;
			n--;
		}
		s1[i] = '\0';
		return (s1);
	}
	return (NULL);
}
