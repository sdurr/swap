int		ft_count_char(char *s, char c)
{
	int		count;

	count = 0;
	if (s)
	{
		while (*s != '\0')
		{
			while (*s == c && *s != '\0')
				s++;
			while (*s != c && *s != '\0')
				s++;
			count++;
		}
		return (count);
	}
	return (count);
}
