#include <string.h>

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*rt;

	i = 0;
	rt = (char *)s;
	if (rt)
	{
		while (n > 0)
		{
			rt[i] = '\0';
			i++;
			n--;
		}
	}
}
