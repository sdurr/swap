#include "push.h"

void		tri_insertion(int *t, int n)
{
	int		i;
	int		p;
	int		j;
	int		x;

	i = 1;
	while (i < n)
	{
		x = t[i];
		p = i - 1;
		while (t[p] > x && p >0)
			p--;
		p++;
		j = i - 1;
		while (j >= p)
		{
			t[j + 1] = t[j];
			j--;
		}
		t[p] = x;
		i++;
	}
}
